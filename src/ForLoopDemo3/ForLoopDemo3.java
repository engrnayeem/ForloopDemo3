public class ForLoopDemo3 {
    public static void main(String[]args)
            
    {
     int sum=0;
     
     for(int x=1;x<=10;x++)
     {
     
     sum=sum+x;
     
     }
     
     
     System.out.println("The sum of 1 to 10 number by using for loop is:"+sum);
    
    
    
    }
    
}
